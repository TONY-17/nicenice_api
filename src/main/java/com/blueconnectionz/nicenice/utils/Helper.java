    public static String driverProfileImage(List<Document> documents) throws Exception {
        String result = null;
        int numThreads = Runtime.getRuntime().availableProcessors();
        ExecutorService executor = Executors.newFixedThreadPool(numThreads);
        List<Callable<String>> tasks = createTasks(documents);
        List<Future<String>> futures = executor.invokeAll(tasks);
        for (Future<String> future : futures) {
            String url = future.get();
            if (url != null) {
                result = url;
                break;
            }
        }
        executor.shutdown();
        return result;
    }

    private static List<Callable<String>> createTasks(List<Document> documents) {
        List<Callable<String>> tasks = new ArrayList<>();
        for (Document doc : documents) {
            tasks.add(() -> {
                if (doc.getUrl().contains(".png") || doc.getUrl().contains(".jpeg") || doc.getUrl().contains(".jpg")) {
                    return doc.getUrl();
                } else {
                    return null;
                }
            });
        }
        return tasks;
    }


